package helpers;
public class yvars 
{
 
     
    public static String yitostring(int n)
    {
        return Integer.toString(n);
    }
     
    public static int ystoint(String s)
    {
        return Integer.parseInt(s);
    }
     
    public double ystodouble(String s)
    {
        return Double.parseDouble(s);
    }
    public String ydtostring(double n)
    {
        return String.valueOf(n);
    }
     
 
}//end yvars